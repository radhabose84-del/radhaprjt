    }

    // ─────────────────────────────────────────────────────────────────────────
    // SECTION 7 — DEACTIVATE (Update IsActive=0, NOT delete)
    // ─────────────────────────────────────────────────────────────────────────

    [Fact, TestPriority(60)]
    public async Task TC060_Deactivate_Update_IsActive0_Returns200()
    {
        _createdId.Should().BeGreaterThan(0);

        var resp = await _f.Client.PutAsJsonAsync(BaseRoute, new
        {
