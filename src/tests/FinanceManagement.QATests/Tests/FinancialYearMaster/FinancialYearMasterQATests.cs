        });

        await AssertOkAsync(resp);
    }

    [Fact, TestPriority(61)]
    public async Task TC061_Deactivate_Re_Activate_Returns200()
    {
        var resp = await _f.Client.PutAsJsonAsync(BaseRoute, new
        {
            id = _createdId,
