            isActive = 1
        });

        await AssertOkAsync(resp);
    }

    // ─────────────────────────────────────────────────────────────────────────
    // SECTION 8 — DELETE (soft) + already-deleted guard
    // ─────────────────────────────────────────────────────────────────────────

    [Fact, TestPriority(80)]
    public async Task TC080_Delete_HappyPath_Returns200()
    {
        _createdId.Should().BeGreaterThan(0);

        var resp = await _f.Client.DeleteAsync($"{BaseRoute}?id={_createdId}");
        await AssertOkAsync(resp);
    }

    [Fact, TestPriority(81)]
    public async Task TC081_Delete_AlreadyDeleted_Returns400()
    {
        // TC080 already soft-deleted _createdId
        var resp = await _f.Client.DeleteAsync($"{BaseRoute}?id={_createdId}");
        // Server may return 400 (validator says NotFound) or 200 with isSuccess=false
        resp.StatusCode.Should().BeOneOf(HttpStatusCode.OK, HttpStatusCode.BadRequest);
    }

    [Fact, TestPriority(82)]
    public async Task TC082_Delete_NonExistent_Returns400()
    {
        var resp = await _f.Client.DeleteAsync($"{BaseRoute}?id=999999");
        resp.StatusCode.Should().BeOneOf(HttpStatusCode.OK, HttpStatusCode.BadRequest);
    }
}
